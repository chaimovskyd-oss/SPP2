export function searchEmojis(query: string): EmojiItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return EMOJI_DATA;
  return EMOJI_DATA.filter(
    (e) =>
      e.name.includes(q) ||
      e.keywords.some((k) => k.includes(q)) ||
      e.id.includes(q)
  );
}

export function getEmojisByCategory(category: EmojiCategory): EmojiItem[] {
  return EMOJI_DATA.filter((e) => e.category === category);
}
